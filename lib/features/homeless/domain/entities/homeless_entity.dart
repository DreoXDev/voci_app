class HomelessEntity {
  final String id;
  final String age;
  final String area;
  final String description;
  final String gender;
  final String location;
  final String name;
  final String nationality;
  final String pets;
  final String status;

  HomelessEntity({
    required this.id,
    required this.age,
    required this.area,
    required this.description,
    required this.gender,
    required this.location,
    required this.name,
    required this.nationality,
    required this.pets,
    required this.status,
  });
}